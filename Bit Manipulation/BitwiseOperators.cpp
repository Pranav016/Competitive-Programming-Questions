/*

Bitwise Operators-
& - AND
| - OR
~ - NOT
^ - XOR 

Some Properties of XOR:
- Exactly one bit is 1
- XOR of two same numbers is 0
- 5 ^ 7 ^ 5 = 7, 5 gets cancelled out
- XOR with 0 is number itself


Shift Operators-
1. << - left shift
Inserts 0s from the left side and that many bits from the right side get discarded.
Left shift works as multiplication with 2
Example. 5 << 1 = 10


2. >> - right shift
Inserts 0s from the right side and that many bits from the left side get discarded.
Right shift works as division with 2
Example. 10 >> 1 = 5

*/